function updateTeamInfo(teamId) {
  const teamData = data[teamId][0];

  // Xóa nội dung cũ
  document.getElementById("nation").innerHTML = '';
  document.getElementById("ovr").innerHTML = '';
  document.getElementById("goals").innerHTML = '';
  document.getElementById("balls-recovered").innerHTML = '';
  document.getElementById("distance-covered").innerHTML = '';
  document.getElementById("goals-conceded").innerHTML = '';
  document.getElementById("tackles-won").innerHTML = '';
  document.getElementById("yellow-cards").innerHTML = '';
  document.getElementById("possession").innerHTML = '';
  document.getElementById("clean-sheets").innerHTML = '';
  document.getElementById("red-cards").innerHTML = '';
  document.getElementById("saves").innerHTML = '';
  document.getElementById("passing-accuracy").innerHTML = '';

  // Thêm hình cờ và tên đội
  let img = document.createElement("img");
  img.classList.add("nationflag"); // Thay "nationflag" bằng lớp CSS thực tế nếu có
  img.setAttribute("src", teamData.flag);

  let name = document.createElement("h1");
  name.classList.add("flagname"); // Thay "flagname" bằng lớp CSS thực tế nếu có
  name.innerText = teamData.name;

  document.getElementById("nation").appendChild(img);
  document.getElementById("nation").appendChild(name);

  // Thêm thông tin tổng quan
  let card = document.createElement("div");
  card.classList.add("match-played");

  let numOfmatch = document.createElement("h2");
  numOfmatch.innerText = teamData.match_played;

  let matchplayed = document.createElement("h3");
  matchplayed.innerText = "Match played";

  card.appendChild(numOfmatch);
  card.appendChild(matchplayed);

  document.getElementById("ovr").appendChild(card);

  let card2 = document.createElement("div");
  card2.classList.add("ovr-stats");

  function createWDLCard(number, text) {
    let card = document.createElement("div");
    card.classList.add("wdl");

    let numberElem = document.createElement("h3");
    numberElem.innerText = number;

    let textElem = document.createElement("p");
    textElem.innerText = text;

    card.appendChild(numberElem);
    card.appendChild(textElem);

    return card;
  }

  card2.appendChild(createWDLCard(teamData.match_won, "Won"));
  card2.appendChild(createWDLCard(teamData.match_drawn, "Drawn"));
  card2.appendChild(createWDLCard(teamData.match_lost, "Lost"));

  document.getElementById("ovr").appendChild(card2);

  // Thêm các chỉ số khác
  document.getElementById("goals").innerHTML = `<h3>${teamData.goals}</h3><h4>Goals</h4>`;
  document.getElementById("balls-recovered").innerHTML = `<h3>${teamData.balls_recovered}</h3><h4>Balls recovered</h4>`;
  document.getElementById("distance-covered").innerHTML = `<h3>${teamData.distance_covered}</h3><h4>Distance covered (km)</h4>`;
  document.getElementById("goals-conceded").innerHTML = `<h3>${teamData.goal_conceded}</h3><h4>Goals conceded</h4>`;
  document.getElementById("tackles-won").innerHTML = `<h3>${teamData.tackles_won}</h3><h4>Tackles won</h4>`;
  document.getElementById("yellow-cards").innerHTML = `<h3>${teamData.yellow_card}</h3><h4>Yellow cards</h4>`;
  document.getElementById("possession").innerHTML = `<h3>${teamData.possession}</h3><h4>Possession (%)</h4>`;
  document.getElementById("clean-sheets").innerHTML = `<h3>${teamData.clean_sheet}</h3><h4>Clean sheets</h4>`;
  document.getElementById("red-cards").innerHTML = `<h3>${teamData.red_card}</h3><h4>Red cards</h4>`;
  document.getElementById("saves").innerHTML = `<h3>${teamData.saves}</h3><h4>Saves</h4>`;
  document.getElementById("passing-accuracy").innerHTML = `<h3>${teamData.passing_accuracy}</h3><h4>Passing accuracy (%)</h4>`;
}

// Thêm sự kiện thay đổi cho dropdown
const teamSelect = document.getElementById("team-select");
if (teamSelect) {
  teamSelect.addEventListener("change", function() {
    updateTeamInfo(this.value);
  });

  // Cập nhật thông tin cho đội đầu tiên khi tải trang
  updateTeamInfo(teamSelect.value);
}
